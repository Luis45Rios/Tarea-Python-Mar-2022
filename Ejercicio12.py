"""EJERCICIO 12.- Programa que lea un carácter por teclado y compruebe si es una 
letra mayúscula."""

let=str(input('ingrese una letra'))
 
if let=='a'<=let<='z':
    print("la letra es miniscula")
else:
    let!='a'<=let<='z'
    print("no es nada")
if let=='A'<=let<='Z':
    print("la letra es mayuscula")
else:
    let!='A'<=let<='Z'
    print("no es nada")